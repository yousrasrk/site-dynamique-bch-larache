<div class="entry p-5" >
    <p style="text-align:justify;"><a name="_Toc311639376"></a><a name="_Toc307731556"></a><span style="color:#000000;"><strong> </strong>La mission du service d&rsquo;hygiène alimentaire  est la prévention et la protection du consommateur contre les toxi-infections et les maladies transmissibles par les produits alimentaires solides et liquides, en veillant sur leurs salubrité et sécurité.</span></p>
   <p style="text-align:justify;"><span style="color:#000000;">         Ce service assure plusieurs fonctions notamment :</span></p>
  <ul style="text-align:justify;" class="b" >
 <li style="text-align:left;"><span style="color:#000000;">Animation de séances d’éducation sanitaires au profit des gérants de ces établissements et du personnel manipulateur des denrées alimentaires;</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Inspections systématiques, permanentes et régulières des commerces alimentaires;</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Inspections motivées par des plaintes suite aux toxi-infections alimentaires;</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Prélèvements de denrées alimentaires et d’eau potable au niveau de ces commerces en vue de contrôler  leur niveau de salubrité;</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Saisie et destruction des denrées alimentaires non conformes ou impropres à la consommation;</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Etablissement de procès-verbaux d’inspection comportant les recommandations d’hygiène à satisfaire;</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Etablissement de mises en demeure découlant des comptes rendus d’inspection;</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Propositions de fermeture des commerces n’ayant pas satisfait les recommandations stipulées par les mises en demeure;</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Participation à diverses commissions avec les services extérieurs (Telles que la Commission préfectorale, la Commission touristique, la Commission mixte, etc.);</span></li>
 <li style="text-align:left;"><span style="color:#000000;">Délivrance de cartes sanitaires après des examens de coprologie parasitologie des selles et radiographie pulmonaire.</span></li>
 </ul>
 <p style="text-align:justify;"><span style="color:#000000;">Pour la réalisation des orientations  des  plans d’actions annuels élaborés par le service d’hygiène alimentaire, ce dernier a axé son action en premier lieu sur le renforcement de la coordination et de la communication entre le service d’hygiène alimentaire et les antennes d’hygiène relevant de la division d’hygiène et salubrité publique au niveau des différents arrondissements de la ville de Larache, et en deuxième lieu sur la vulgarisation des bonnes pratiques d’hygiène  alimentaire auprès des professionnels et du personnel. En effet l’action entreprise par le service d’hygiène alimentaire s’articule sur deux volets principaux à savoir :</span></p>
 <ul style="text-align:justify;"class="b">
 <li><span style="color:#000000;">Une campagne annuelle de grande envergure de recensement et d’actualisation des données relative aux établissements alimentaires ;</span></li>
 <li><span style="color:#000000;">Une campagne parallèle de sensibilisation et d’éducation sanitaire au profit des professionnels de l’alimentaire.</span></li>
 </ul>
 <p style="text-align:justify;"><span style="color:#000000;">A cet effet, le service a établi une fiche d’inspection pour ces établissements permettant la collecte des données relatives à l’état hygiénique du local, le matériel utilisé et le dossier médical du personnel.</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">Les inspecteurs d’hygiène relevant de la DHSP  sont habilités à procéder à des visites d’inspection régulières des établissements assujettis à la réglementation : cafés, glaciers, restaurants, snacks,  sandwicheries, magasins d’alimentation, supermarchés, boulangeries, boucheries, poissonneries, marchés de détail, etc.. Cette démarche de contrôle s’accompagne systématiquement d’une action de prévention et d’information. Ceci pour améliorer la prise de conscience des professionnels sur la nécessité de respecter les règles d’hygiène élémentaires.</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">Les contrôles portent sur la propreté générale, la conformité des locaux, l’hygiène du personnel ainsi que sa formation, le respect des températures de conservation des denrées et stockage, les techniques de travail,  etc..</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">En plus, cette attention particulière se traduit par des inspections répétitives au niveau de certains  de ces établissements dont les gérants affichent un laxisme eu  égard aux recommandations d’hygiène demandées par les inspecteurs du service.</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">Durant ces inspections régulières, une multitude des pratiques déplorables de manipulation des denrées alimentaires sont relevées, poussant le service à mener une campagne de sensibilisation et d’éducation sanitaire annuelle au profit des propriétaires ou gérants et du personnel manipulateur des denrées alimentaires des établissements  à vocation alimentaire portant sur :</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">&#8211;              <strong>L’état hygiénique du local :</strong></span></p>
 <ul style="text-align:justify;"class="b"> 
 <li><span style="color:#000000;">Toutes les installations et équipements liés à la production, la manipulation des denrées alimentaires devant être conçus de manière à faciliter leur entretien, leur nettoyage et leur désinfection.</span></li>
 <li><span style="color:#000000;">Le raccordement obligatoire de ces locaux aux réseaux d’eau, d’électricité et d’égouts de la ville ;</span></li>
 <li><span style="color:#000000;">Des opérations de désinfection et de désinsectisation périodiques  des locaux ;</span></li>
 <li><span style="color:#000000;">Le maintien  permanent du local en parfait état de propreté.</span></li>
 </ul>
 <p style="text-align:justify;"><span style="color:#000000;"> </span></p>
 <p style="text-align:justify;"><span style="color:#000000;">&#8211;              <strong>Le matériel utilisé et les équipements : </strong></span></p>
 <ul style="text-align:justify;" class="b">
 <li><span style="color:#000000;">Les comptoirs de vente, tables, étales et tout matériel analogue en contact avec les denrées alimentaires doivent être revêtus de matériaux imperméables et lisses maintenus en état de propreté  de façon permanente ;</span></li>
 <li><span style="color:#000000;">L’équipement des locaux de moyens de réfrigération adéquats et de dispositifs appropriés de manière à protéger les denrées alimentaires du soleil, des intempéries et des pollutions de toute nature.</span></li>
 </ul>
 <p style="text-align:justify;"><span style="color:#000000;">&#8211;              <strong>Le personnel manipulateur :</strong></span></p>
 <ul style="text-align:justify;"class="b">
 <li><span style="color:#000000;">Les personnes appelées à manipuler les denrées alimentaires, tant au cours de leur collecte, préparation, traitement, transformation, conditionnement, emballage, transport, entreposage que pendant leur exposition, mise en vente et distribution, sont  astreintes à la plus grande propreté corporelle et vestimentaire sous la responsabilité de l’employeur, car la manipulation des denrées est interdites aux personnes susceptibles de les contaminer, notamment celles qui sont atteintes d’infections cutanées, muqueuses, respiratoires ou intestinales. Les personnes affectées à la manipulation de ces denrées doivent être soumises à des visites médicales périodiques (coprologie-parasitologie des selles et la radio-photo pulmonaire) à raison de deux fois par an.</span></li>
 </ul>
 <p style="text-align:justify;"><span style="color:#000000;">Parallèlement aux campagnes d’inspection, de sensibilisation et d’éducation sanitaire, les inspecteurs d’hygiène relevant du SHA ont procédé à la destruction de denrées alimentaires impropres à la consommation et à la saisie du matériel et ustensile défectueux (poêle, casserole, marmite, couteau, assiette, etc.).</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">Les opérations de contrôle d’hygiène menées par les inspecteurs d’hygiène de la DHSP incluent, le cas échéant, des prélèvements d’échantillons de denrées alimentaires en vue d’analyses bactériologiques et toxicologiques.</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">Ces échantillons  sont prélevés dans le respect des normes en vigueur et mis dans des bocaux stérilisés ou des sacs en plastique appropriés et sont acheminés vers l’Institut National d’hygiène de Larache pour analyses selon un marché cadre qui lie la commune urbaine de Larache et cet institut.</span></p>
  <p style="text-align:justify;"><span style="color:#000000;">Ces prélèvements sont opérés selon un calendrier hebdomadaire  précisant le jour  du prélèvement et l’arrondissement concerné.</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">La contamination microbiologique des aliments est liée essentiellement à la présence d’un taux des germes de souillure (coliformes fécaux, coliformes totaux et des germes totaux) et la prolifération des bactéries pathogènes. Ce problème se pose essentiellement dans la restauration collective ce qui peut provoquer des toxi-infections alimentaires.</span></p>
 <p style="text-align:justify;"><span style="color:#000000;">Le SHA alimentaire à pour rôle de prendre les mesures de salubrité nécessaires pour lutter contre la propagation des maladies infectieuses particulièrement à travers le prélèvement d’échantillons de denrées alimentaires pour les soumettre aux analyses appropriés.</span></p>
